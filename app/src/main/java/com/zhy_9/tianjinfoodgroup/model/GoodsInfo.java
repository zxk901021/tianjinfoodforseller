public class GoodsInfo {

    private String goodsThums;

    private String goodsName;

    private String shopPrice;

    private String goodsStack;

    private String goodsSn;

    private String saleCount;

    public String getGoodsThums() {
        return goodsThums;
    }

    public void setGoodsThums(String goodsThums) {
        this.goodsThums = goodsThums;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getShopPrice() {
        return shopPrice;
    }

    public void setShopPrice(String shopPrice) {
        this.shopPrice = shopPrice;
    }

    public String getGoodsStack() {
        return goodsStack;
    }

    public void setGoodsStack(String goodsStack) {
        this.goodsStack = goodsStack;
    }

    public String getGoodsSn() {
        return goodsSn;
    }

    public void setGoodsSn(String goodsSn) {
        this.goodsSn = goodsSn;
    }

    public String getSaleCount() {
        return saleCount;
    }

    public void setSaleCount(String saleCount) {
        this.saleCount = saleCount;
    }
}
